package pkg305_project;


public class User {
    private int ID;
    private String password;

    public User(int ID, String password) {
        this.ID = ID;
        this.password = password;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
    
    
               
}
